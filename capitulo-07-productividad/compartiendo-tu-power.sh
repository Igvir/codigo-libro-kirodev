#### Compartiendo Tu Power

Una vez creado, puedes compartir tu Power con el equipo:

**Opción 1: Repositorio Git**

1. Hospeda el Power en un repositorio Git
2. En Kiro: Powers > Import from Git
3. Pega la URL del repositorio
4. El Power se instala para el usuario

